#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		len1;
	int		len2;
	int		i;
	int		j;
	char	*result;

	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result = (char *)malloc(len1 + len2 + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (i < len1)
	{
		result[i] = s1[i];
		i ++;
	}
	j = 0;
	while (j < len2)
	{
		result[len1 + j] = s2[j];
		j ++;
	}
	result[len1 + len2] = '\0';
	return (result);
}
